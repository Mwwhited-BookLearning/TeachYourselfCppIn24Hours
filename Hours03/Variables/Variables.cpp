
#include <iostream>

using namespace std;

int main()
{
	cout << "The size of an 'int' is " << sizeof(int) << " bytes\n";
	cout << "The size of a 'short' is " << sizeof(short) << " bytes\n";
	cout << "The size of a 'long' is " << sizeof(long) << " bytes\n";
	cout << "The size of a 'char' is " << sizeof(char) << " bytes\n";
	cout << "The size of a 'bool' is " << sizeof(bool) << " bytes\n";
	cout << "The size of a 'float' is " << sizeof(float) << " bytes\n";
	cout << "The size of a 'double' is " << sizeof(double) << " bytes\n";

	cout << "The size of an 'unsigned int' is " << sizeof(unsigned unsigned int) << " bytes\n";
	cout << "The size of a 'unsigned short' is " << sizeof(unsigned short) << " bytes\n";
	cout << "The size of a 'unsigned long' is " << sizeof(unsigned long) << " bytes\n";
	cout << "The size of a 'unsigned char' is " << sizeof(unsigned char) << " bytes\n";
}

